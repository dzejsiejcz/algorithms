public class BWT {

    public String s;
    public int n;

    public BWT(String s, int n) {
        this.s = s;
        this.n = n;
    }

    @Override
    public String toString() {
        return "BWT{" +
                "s='" + s + '\'' +
                ", n=" + n +
                '}';
    }
}
